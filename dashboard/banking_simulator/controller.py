"""
Banking Operations Simulator — CONTROLLER
==========================================
Wires Model + View. User selects only the PERIOD (Day/Week/Month/Year).
Number of customers per day is randomly generated by the system within
realistic ranges (not user-controlled).
"""
import json
import random
from . import ml_engine
from . import characters
from .scene import build_simulator

PERIOD_DAYS = {"Day": 1, "Week": 7, "Month": 30, "Year": 365}

# System-controlled realistic ranges (not user-controlled)
LOAN_PER_DAY = (2, 12)        # 2-12 loan applicants per day
CASHIER_PER_DAY = (10, 30)    # 10-30 cashier customers per day


def build_banking_simulator(period="Day", model_name="Logistic Regression",
                            mode="full", seed=None):
    """
    period   : "Day" | "Week" | "Month" | "Year"
    mode     : "full"     -> show cashier + loan + manager (Loan Counter page)
               "cashier"  -> emphasize cashier counter (Simulation Dashboard page)
    """
    if seed is not None:
        random.seed(seed)
    bundle = ml_engine.load_model(model_name)
    days = PERIOD_DAYS.get(period, 1)
    day_data = []
    for d in range(1, days + 1):
        loan_n = random.randint(*LOAN_PER_DAY)
        cash_n = random.randint(*CASHIER_PER_DAY)
        day_data.append(ml_engine.prepare_day(bundle, d, loan_n, cash_n))

    cust_svgs = [characters.customer_svg(i) for i in range(6)]
    return build_simulator(
        officer_svg=characters.LOAN_OFFICER_SVG,
        cashier_svg=characters.CASHIER_SVG,
        manager_svg=characters.MANAGER_SVG,
        customer_svgs=cust_svgs,
        day_data_json=json.dumps(day_data),
        mode=mode,
    )


def aggregate_stats(period="Day", model_name="Logistic Regression", seed=42):
    """
    Pre-compute aggregate statistics for the chosen period — used by the
    Simulation Dashboard page to show graphs alongside the live scene.
    """
    random.seed(seed)
    bundle = ml_engine.load_model(model_name)
    days = PERIOD_DAYS.get(period, 1)
    per_day = []
    total_loan = total_cash = total_app = total_rej = 0
    for d in range(1, days + 1):
        loan_n = random.randint(*LOAN_PER_DAY)
        cash_n = random.randint(*CASHIER_PER_DAY)
        day = ml_engine.prepare_day(bundle, d, loan_n, cash_n)
        appr = sum(1 for a in day["loan_apps"] if a["decision"] == "APPROVED")
        rej = loan_n - appr
        per_day.append({"day": d, "loans": loan_n, "cashier": cash_n,
                        "approved": appr, "rejected": rej})
        total_loan += loan_n; total_cash += cash_n
        total_app += appr; total_rej += rej
    return {
        "period": period, "days": days,
        "per_day": per_day,
        "total_loan": total_loan, "total_cashier": total_cash,
        "total_approved": total_app, "total_rejected": total_rej,
        "approval_rate": round(total_app/total_loan*100, 1) if total_loan else 0,
    }
